/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
 public:
  vector<vector<int>> levelOrder(TreeNode* root) {
    auto result = vector<vector<int>>();
    auto curr_level = vector<TreeNode*>();
    auto next_level = curr_level;
    if (root)
      next_level.emplace_back(root);
    while (!next_level.empty()) {
      swap(curr_level, next_level);
      next_level.clear();
      result.emplace_back();
      auto& values = result.back();
      for (auto* node : curr_level) {
        values.emplace_back(node->val);
        if (node->left)
          next_level.emplace_back(node->left);
        if (node->right)
          next_level.emplace_back(node->right);
      }
    }
    return result;
  }
};
